#include <fractol.h>

t_col	get_color(int n, t_env *env)
{
	t_col	ret;

	if (env->color)
	{
		return ((t_col){sin(0.016 * n + 4) * 180 + 25,
			sin(0.013 * n + 2) * 230 + 25, sin(0.01 * n + 1) * 230 + 25});
	}
	else
		return ((t_col){n & 0xFF, n & 0xFF, n & 0xFF });
	return (ret);
}
